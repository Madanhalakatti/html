public class Switch {
    public static void main(String[] args) {
        char animal='b';
        switch (animal) {
            case 'a':System.out.println("the animal is cat");
                
                break;
                case 'b':System.out.println("the animal is tiger");
                break;
                case 'c':System.out.println("the animal is lion");
                break;
        
            default:System.out.println("invalid animal");
                break;
        }

    }

}
